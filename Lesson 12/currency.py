def to_euro(dollar):
    """ This function converts USD to Euro"""

    euro = dollar * .81
    return euro

def to_yen(dollar):
    """ This function converts USD to Yen"""

    yen = dollar * 106.45
    return yen

def to_peso(dollar):
    """ This function converts USD to Peso"""

    peso = dollar * 18.58
    return peso